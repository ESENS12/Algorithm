class Solution {
    public Node connect(Node root) {
        if(root == null) return null;
        Node pre = root;
        
        while(pre.left != null){
            Node curr = pre;
            
            while(curr != null){
                curr.left.next = curr.right;
                if(curr.next != null){
                    curr.right.next = curr.next.left;
                }
                curr = curr.next;
            }
            pre = pre.left;
        }
        return root;
    }
}
